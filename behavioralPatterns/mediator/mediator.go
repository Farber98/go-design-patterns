package mediator

/*
MEDIATOR:
- Will be in between two types to exchange information.
- Avoid tight coupling.
- Mediator act as the type in charge of exchanging communication between 2 objects.
- Objects dont need to know each other and can change more freely.
- Pattern that maintains which objects give what information is Mediator.

OBJECTIVE:
- Loose coupling and encapsulation between 2 objects that must communicate between them.
- Reduce amount of dependencies of a particular type to the minimum by passing these needs to the mediator pattern.


*/
